    public static void setVariables(Battle f, boolean e, GregorianCalendar d)
    {
        FIGHT = f;
        EDIT = e;
        DATE = d;
        AddOrEditBattleFrame.fillTime();
    }
    
    private static void fillTime()
    {
        int hours = 24*2;
        GregorianCalendar fill = new GregorianCalendar(DATE.get(Calendar.YEAR), 
                                                       DATE.get(Calendar.MONTH), 
                                                       DATE.get(Calendar.DAY_OF_MONTH), 
                                                       0,0);
        String add = "";
        SimpleDateFormat fmt = new SimpleDateFormat("HH:mm");
        for(int i = 0; i < hours; i++)
        {
            add = fmt.format(fill.getTime());
            timeCombo.addItem(add);
            fill.add(Calendar.MINUTE, 30);
        }
    }
    
    private static void addToSchedule(Battle fight)
    {
        Schedule sched = new Schedule();
        if(sched.loadScheduleFromStorageFile())
        {
           if(sched.addToSchedule(fight))
           {
               sched.writeScheduleToStorageFile();
           }
        }
    }
    
    private static void editBattle(Battle edit)
    {
        Schedule sched = new Schedule();
        if(sched.loadScheduleFromStorageFile())
        {
           if(sched.updateBattleInSchedule(FIGHT, edit))
           {
               sched.writeScheduleToStorageFile();
           }
        }
    }
    
    private static void makeDateFromTime(String time)
    {
        String sep = ":";
        String[] newTime = new String[2];
        newTime = time.split(sep);
        int hour = Integer.parseInt(newTime[0]);
        int min = Integer.parseInt(newTime[1]);
        
        DATE = new GregorianCalendar(DATE.get(Calendar.YEAR), 
                                     DATE.get(Calendar.MONTH), 
                                     DATE.get(Calendar.DAY_OF_MONTH), 
                                     hour,min);
    }

