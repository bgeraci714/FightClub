    private void submitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitButtonActionPerformed
        String name1 = firstOpponentField.getText();
        String name2 = secondOpponentField.getText();
        if(name1.equals("") || name2.equals(""))
            JOptionPane.showMessageDialog(null, "Must enter name.", "ERROR", JOptionPane.ERROR_MESSAGE);
        else
        {
            int b = bracketCombo.getSelectedIndex();
            String bracket = bracketCombo.getItemAt(b);
            int t = timeCombo.getSelectedIndex();
            String time = timeCombo.getItemAt(t);
            AddOrEditBattleFrame.makeDateFromTime(time);
            Battle make = new Battle(name1, name2, bracket, DATE);
            if(EDIT)
            {
                AddOrEditBattleFrame.editBattle(make);
            }
            else
            {
                AddOrEditBattleFrame.addToSchedule(make);
            }
            ScheduleFrame.setBattleList(DATE);
        }
        this.setVisible(false);
    }//GEN-LAST:event_submitButtonActionPerformed

